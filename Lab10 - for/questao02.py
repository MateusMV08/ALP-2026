# range(10) # inicio=0, fim=10, pulo=1
for variavel in range(10):
    print(variavel)

# range(1, 5) # inicio=1, fim=5, pulo=1
for variavel in range(1, 5):
    print(variavel)
    
# range(0, 10, 2) # inicio=0, fim=10, pulo=2
for variavel in range(0, 10, 2):
    print(variavel)

# range(10, 0, -1) # inicio=10, fim=0, pulo=-1
for variavel in range(10, 0, -1):
    print(variavel)
